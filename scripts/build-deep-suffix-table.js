const used = new Set();

function pickRandom() {
    for (let i = 0; i < 30000; i++) {
        const a = POOL[(Math.random() * POOL.length) | 0];
        const b = POOL[(Math.random() * POOL.length) | 0];
        if (a === b) continue;
        const s = a + b;
        if (used.has(s)) continue;
        used.add(s);
        return s;
    }
    throw new Error('无法生成足够唯一后缀');
}

