function buildTable() {
    const table = {};
    for (const line of LINE_KEYS) {
        const seeds = SEEDS[line];
        table[line] = [];
        for (let t = 0; t < 8; t++) {
            const row = [];
            for (let q = 0; q < 5; q++) {
                const s = seeds[t][q];
                if (used.has(s)) {
                    console.error('种子重复:', line, t, q, s);
                    process.exit(1);
                }
                used.add(s);
                row.push(s);
            }
            table[line].push(row);
        }
    }
    return table;
}

